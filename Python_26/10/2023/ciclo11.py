empleados=int(input("Ingrese la cantidad de empleados ...."))
x=1
while(x<=empleados):
    cantidad=int(input("Ingrese la cantidad de muebles realizados..."))
    meta=(cantidad/20)*100
    print("La cantidad de muebles realizados por el empleado son: ",cantidad)
    print("La meta de cumplimiento de este empleado es : ",meta,"%")
    x=x+1